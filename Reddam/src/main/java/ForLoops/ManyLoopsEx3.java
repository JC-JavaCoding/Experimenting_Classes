package ForLoops;

public class ManyLoopsEx3
{
   public static void main(String coolbeans[])
   {
      //1.1
      for(int i = 10; i > 0; i--)
      {
         System.out.print(i + " ");
      }
      
      System.out.println();
      //1.2
      for(int i = 25; i < 31; i++)
      {
         System.out.print(i + " ");
      }
      
      System.out.println();
      //1.3
      for(int i = 0; i > -21; i--)
      {
         System.out.print(i + " ");
      }
   }
}